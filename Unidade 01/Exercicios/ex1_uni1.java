public class ex1_uni1 {
    public static void main(String[] args) {
        // Declarando e atribuindo valores às variáveis
        int numeroInteiro = 10;
        float numeroFlutuante = 5.5f; // Números decimais em Java requerem um sufixo "f" no final
        String texto = "Olá, mundo!";

        // Exibindo os valores das variáveis na tela
        System.out.println("Número inteiro: " + numeroInteiro);
        System.out.println("Número de ponto flutuante: " + numeroFlutuante);
        System.out.println("String: " + texto);
    }
}
